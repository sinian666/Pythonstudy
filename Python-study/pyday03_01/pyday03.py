#元组：tuple
'''
1、声明元组
    变量名=(数据1，数据2)
2、特点:没有增删改，只有查这一个功能，且用法与列表相同。
例子：
a=(37.2,33,22,"abdc",True)  #定义一个元组
print(a)# 输出这个元组【(37.2, 33, 22, 'abdc', True)】
print(len(a))#输出这个元组的长度【5】
print(a[1])#输出元组中下标为‘1’的数据【33】
for i in range(0,len(a)):#按下标循环数据
    print(a[i],end=' ')#输出下标对应的数据【37.2 33 22 abdc True】
print()#表示换行
for i in a:#让数据进行循环
    print(i,end=' ')#输出对应数据，并在同一行显示【37.2 33 22 abdc True】
print()#表示换行'''


