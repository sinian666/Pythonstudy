#四、冒泡排序  重要
'''
1、依次比较相邻的两个数，如果前面的数大于后面的数，
交换这两个数，最后按照从小到大的顺序进行排序。
例：
6,18,10,23,12,1
    第一轮：6,18,10,23,12,1
    第一次:6<18
    第二次：18>10
     6,10,18,23,12,1
    第三次：18<23
    第四次：23>12
     6,10,18,12,23,1
    第五次：23>1
     6,10,18,12,1,23
第二轮：6,10,18,12,1
    第一次:6<10
    第二次：10<18
    第三次：18>12
     6,10,12,18,1
    第四次：18>1
     6,10,12,1,18
第三轮：6,10,12,1
    第一次:6<10
    第二次：10<12
    第三次：12>1
     6,10,1,12
第四轮：6,10,1
    第一次:6<10
    第二次：10>1
     6,1,10
第五轮：6,1
    第一次：6>1
     1,6
总结：     
轮数   比较次数
0       5
1       4
2       3
3       2
4       1
轮数+比较次数=5   次数=5-轮数
例子：
sn=[329,18,27,10,28,3,76,17]
#轮数
for  i in range(0,len(sn)-1):#for  i in range(0,7):
    #次
    for j in range(0,len(sn)-1-i):#for j in range(0,7-i):
        #比较
        if sn[j]>sn[j+1]:
            #交换
            sn[j],sn[j+1]=sn[j+1],sn[j]
print(sn)  #【[3, 10, 17, 18, 27, 28, 76, 329]】'''
